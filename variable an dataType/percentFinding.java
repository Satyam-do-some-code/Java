public class percentFinding {
    public static void main(String[] args) {
        double x1 = 90;
        double x2 = 91;
        double x3 = 92;
        double x4 = 93;
        double x5 = 94;

        double result = ((x1 + x2 + x3 + x4 + x5)/500 ) * 100;

        System.out.println(result+"%");
    }
}
